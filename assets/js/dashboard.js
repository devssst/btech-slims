// PENDING BOOK CONTEXT FOR DUPE TITLE CONFIRMATION
let pendingBookEntry = null;

// SAVE NEW BOOK
function saveNewBook(forceConfirmed = false) {
    let title = document.getElementById("new-book-title").value;
    let author = document.getElementById("new-book-author").value;
    const isbn = document.getElementById("new-book-isbn").value;
    const desc = document.getElementById("new-book-desc").value;
    const quantityRaw = document.getElementById("new-book-quantity").value;

    const registerBtn = document.querySelector("#add-book-modal .btn-success");

    // QUANTITY VALIDATION - must be a positive integer, max 999
    const quantity = parseInt(quantityRaw, 10);
    if (!quantityRaw || isNaN(quantity) || quantity < 1 || !Number.isInteger(quantity)) {
        triggerModalError("add-book-modal", registerBtn, "INVALID QUANTITY");
        return;
    }
    if (quantity > 999) {
        triggerModalError("add-book-modal", registerBtn, "MAX QUANTITY IS 999");
        return;
    }

    // DUPE CHECK - same ISBN auto-fills title/author
    if (isbn && (!title || !author)) {
        const existing = libraryInventory.find((b) => b.isbn === isbn);
        if (existing) {
            title = existing.title;
            author = existing.author;
            if (!desc && existing.description) {
                document.getElementById("new-book-desc").value = existing.description;
            }
        }
    }

    // SAME TITLE, DIFFERENT ISBN CHECK
    if (!forceConfirmed && title) {
        const titleMatch = libraryInventory.find(
            (b) => b.title.toLowerCase() === title.toLowerCase() && b.isbn !== isbn
        );
        if (titleMatch) {
            pendingBookEntry = { title, author, isbn, desc, quantity };
            openModal("dupe-title-confirm-modal");
            return;
        }
    }

    // CREATE NEW BOOK(S)
    if (title && author && isbn) {
        for (let i = 0; i < quantity; i++) {
            const newBook = {
                id: Date.now() + i,
                title: title,
                author: author,
                isbn: isbn,
                description: desc || "No description provided.",
                status: "available",
                dateAdded: new Date().toISOString().split("T")[0],
            };
            libraryInventory.push(newBook);
        }
        addLog('ADD_BOOK', `Title: "${title}" | ISBN: ${isbn} | Author: ${author} | Quantity: ${quantity}`);
        renderBookTable();
        updateStatistics();
        closeModal("add-book-modal");
        pendingBookEntry = null;
    } else {
        triggerModalError(
            "add-book-modal",
            registerBtn,
            "FILL ALL REQUIRED FIELDS",
        );
    }
}

