// CONFIRM UPDATE STATUS
function confirmUpdateStatus() {
    const newStatus = document.getElementById('update-status-dropdown').value;
    if (!newStatus) {
        const dropdown = document.getElementById('update-status-dropdown');
        dropdown.classList.add('shake');
        setTimeout(() => dropdown.classList.remove('shake'), 150);
        return;
    }
    
    const book = libraryInventory.find(b => b.id === currentViewId);
    if (!book) return;
    
    const bookTitle = book.title;
    const lastTouchStudentId = book.lastTouch ? book.lastTouch.studentId : null;
    const statusDescriptions = {
        'returned': 'Returned (Available)',
        'paid': 'Paid/Resolved (Removed)',
        'resolved': 'Resolved (Removed)'
    };
    
    switch(newStatus) {
        case 'returned':
            book.status = 'available';
            book.damageType = null;
            book.damageDetails = null;
            book.lastTouch = null;
            
            updateBorrowerViolation(lastTouchStudentId, bookTitle, 'Resolved - Returned');
            break;
            
        case 'paid':
            updateBorrowerViolation(lastTouchStudentId, bookTitle, 'Paid / Resolved');
            
            libraryInventory = libraryInventory.filter(b => b.id !== book.id);
            break;
            
        case 'resolved':
            updateBorrowerViolation(lastTouchStudentId, bookTitle, 'Resolved');
            libraryInventory = libraryInventory.filter(b => b.id !== book.id);
            break;
    }
    
    addLog('UPDATE_BOOK_STATUS', `Book: "${bookTitle}" | New Status: ${statusDescriptions[newStatus]}`);
    renderBookTable();
    updateStatistics();
    closeModal('update-status-modal');
    closeModal('view-book-modal');
}

// VIOLATION UPDATE
function updateBorrowerViolation(studentId, bookTitle, newStatus) {
    if (!studentId) return;
    
    const borrower = borrowerDatabase.find(b => b.studentId === studentId);
    if (!borrower) return;
    const violation = borrower.violations.find(v => 
        v.book === bookTitle && 
        (v.status === 'Unresolved' || v.status === 'Pending')
    );
    
    if (violation) {
        violation.status = newStatus;
    }
}

