// LOGGING FUNCTION
function addLog(actionType, details) {
    const timestamp = new Date().toLocaleString('en-US', {
        year: 'numeric',
        month: '2-digit',
        day: '2-digit',
        hour: '2-digit',
        minute: '2-digit',
        second: '2-digit',
        hour12: false
    });
    
    systemLogs.unshift({
        timestamp: timestamp,
        action: actionType,
        details: details
    });
    
    renderLogs();
}

// RENDER LOGS
function renderLogs() {
    const logsContainer = document.getElementById('logs-container');
    if (!logsContainer) return;
    
    if (systemLogs.length === 0) {
        logsContainer.innerHTML = '<div style="padding: 20px; color: #aaa;">No recent activity recorded.</div>';
        return;
    }
    
    let html = '<div class="logs-table-wrapper">';
    html += '<table class="data-table">';
    html += '<thead><tr>';
    html += '<th style="width: 200px;">ACTION</th>';
    html += '<th>DETAILS</th>';
    html += '<th style="width: 180px;">TIMESTAMP</th>';
    html += '</tr></thead>';
    html += '<tbody>';
    
    systemLogs.forEach(log => {
        // Determine badge color class
        let badgeClass = '';
        switch(log.action) {
            case 'LOGIN':
            case 'RETURN':
                badgeClass = 'log-badge-green';
                break;
            case 'ADD_BOOK':
            case 'ADD_BORROWER':
            case 'BORROW':
                badgeClass = 'log-badge-orange';
                break;
            case 'DELETE_BOOK':
            case 'DELETE_BORROWER':
            case 'REPORT_BOOK':
                badgeClass = 'log-badge-red';
                break;
            case 'UPDATE_BOOK_STATUS':
            case 'NOTIFY_BORROWERS':
                badgeClass = 'log-badge-blue';
                break;
            default:
                badgeClass = 'log-badge-default';
        }
        
        html += '<tr>';
        html += `<td><span class="log-action-badge ${badgeClass}">${log.action}</span></td>`;
        html += `<td style="color: var(--tertiary); font-size: 14px;">${log.details}</td>`;
        html += `<td style="font-family: monospace; font-size: 14px;">${log.timestamp}</td>`;
        html += '</tr>';
    });
    
    html += '</tbody></table></div>';
    logsContainer.innerHTML = html;
}

