// CONFIRM
function confirmReportBook() {
    const book = reportContext.book;
    const status = reportContext.status;
    const otherDetails = reportContext.otherDetails;
    const currentDate = new Date().toISOString().split('T')[0];
    
    const previousBorrowerId = book.borrowerId;
    
    const statusMap = {
        'lost': 'lost',
        'torn': 'lost',
        'wet': 'lost',
        'other': 'lost'
    };
    
    book.status = statusMap[status] || 'lost';
    
    book.damageType = status;
    
    // DAMAGE DETAILS
    if (status === 'other') {
        book.damageDetails = otherDetails;
    } else if (status === 'torn') {
        book.damageDetails = 'Torn apart';
    } else if (status === 'wet') {
        book.damageDetails = 'Wet';
    } else if (status === 'lost') {
        book.damageDetails = 'Lost / Missing';
    }
    
    // ADD VIOLATION
    if (book.borrowerId) {
        const borrower = borrowerDatabase.find(b => b.studentId === book.borrowerId);
        
        if (borrower) {
            const violationType = status === 'lost' ? 'Lost Book' : 'Book Damage';
            const violationDesc = status === 'other' ? otherDetails : book.damageDetails;
            
            borrower.violations.push({
                type: violationType,
                book: book.title,
                date: currentDate,
                description: violationDesc,
                damageType: status,
                status: 'Unresolved'
            });
            
            borrower.currentBooks = borrower.currentBooks.filter(b => b.bookId !== book.id);
        }
        
        // LAST TOUCH SETTER
        book.lastTouch = {
            name: book.borrowedBy,
            studentId: book.borrowerId,
            date: currentDate
        };
        
        book.borrowedBy = null;
        book.borrowerId = null;
        book.dueDate = null;
    } else {
        book.lastTouch = {
            name: 'Unknown',
            studentId: 'N/A',
            date: currentDate
        };
    }
    
    const statusDescMap = {
        'lost': 'Lost/Missing',
        'torn': 'Torn Apart',
        'wet': 'Wet',
        'other': otherDetails
    };
    const reportDesc = statusDescMap[status] || status;
    const borrowerInfo = book.lastTouch.name !== 'Unknown' ? ` | Last Borrower: ${book.lastTouch.name}` : '';
    addLog('REPORT_BOOK', `Book: "${book.title}" | Issue: ${reportDesc}${borrowerInfo}`);
    
    renderBookTable();
    renderBorrowerTable();
    updateStatistics();
    
    const bookDetailsModal = document.getElementById('book-details-modal');
    if (bookDetailsModal && !bookDetailsModal.classList.contains('hidden')) {
        viewBookDetails(book.id);
    }
    
    const borrowerDetailsModal = document.getElementById('borrower-details-modal');
    if (borrowerDetailsModal && !borrowerDetailsModal.classList.contains('hidden') && previousBorrowerId) {
        const borrower = borrowerDatabase.find(b => b.studentId === previousBorrowerId);
        if (borrower) {
            viewBorrowerDetails(borrower.studentId);
        }
    }
    
    closeModal('report-confirm-modal');
    
    reportContext.bookName = '';
    reportContext.isbn = '';
    reportContext.status = '';
    reportContext.otherDetails = '';
    reportContext.book = null;
}

