// BOOK INVENTORY PAGE
function calculateTimeDifference(targetDate, isPast = false) {
    const target = new Date(targetDate);
    const today = new Date();
    const diffTime = isPast ? (today - target) : (target - today);
    const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
    
    if (diffDays < 0) {
        return isPast ? "0 days" : "Overdue";
    }
    
    if (diffDays === 0) {
        return "Today";
    } else if (diffDays === 1) {
        return isPast ? "1 day ago" : "1 day left";
    } else if (diffDays < 30) {
        return isPast ? `${diffDays} days ago` : `${diffDays} days left`;
    } else if (diffDays < 365) {
        const months = Math.floor(diffDays / 30);
        return isPast ? `${months} month${months > 1 ? 's' : ''} ago` : `${months} month${months > 1 ? 's' : ''} left`;
    } else {
        const years = Math.floor(diffDays / 365);
        return isPast ? `${years} year${years > 1 ? 's' : ''} ago` : `${years} year${years > 1 ? 's' : ''} left`;
    }
}

// RENDER BOOK TABLE
function renderBookTable() {
    const tableBody = document.querySelector("#book-table tbody");
    const searchTerm = document.getElementById("book-search").value.toLowerCase();
    const statusFilter = document.getElementById("status-filter").value;

    // SEARCH FILTER
    let filtered = libraryInventory.filter((book) => {
        const matchesSearch =
            book.title.toLowerCase().includes(searchTerm) ||
            book.author.toLowerCase().includes(searchTerm) ||
            book.isbn.toLowerCase().includes(searchTerm);

        const matchesStatus = statusFilter === "all" || book.status === statusFilter;
        return matchesSearch && matchesStatus;
    });

    // BOOK SEPARATION IF NOT AVAILABLE
    const availableBooks = filtered.filter((book) => book.status === "available");
    const nonAvailableBooks = filtered.filter(
        (book) => book.status !== "available",
    );

    // ISBN GROUPINGS
    const groupedAvailable = availableBooks.reduce((acc, book) => {
        const key = book.isbn;
        if (!acc[key]) {
            acc[key] = {
                id: book.id,
                title: book.title,
                author: book.author,
                isbn: book.isbn,
                status: book.status,
                count: 1,
                books: [book],
            };
        } else {
            acc[key].count++;
            acc[key].books.push(book);
        }
        return acc;
    }, {});

    // NON AVAILABLE BOOK TO INDIVIDUAL ENTRIES
    const individualNonAvailable = nonAvailableBooks.map((book) => ({
        id: book.id,
        title: book.title,
        author: book.author,
        isbn: book.isbn,
        status: book.status,
        count: 1,
        books: [book],
    }));

    const results = [
        ...Object.values(groupedAvailable),
        ...individualNonAvailable,
    ];

    const priority = { overdue: 4, borrowed: 3, available: 2, lost: 1 };
    results.sort((a, b) => {
        const priorityDiff = priority[b.status] - priority[a.status];
        if (priorityDiff !== 0) return priorityDiff;
        return a.title.localeCompare(b.title);
    });

    tableBody.innerHTML = "";

    if (results.length === 0) {
        tableBody.innerHTML = `
            <tr>
                <td colspan="5" style="text-align:center; padding:40px; color:var(--tertiary);">
                    <i class="fas fa-book" style="font-size:40px; margin-bottom:10px;"></i><br>
                    No books found
                </td>
            </tr>`;
        return;
    }

    results.forEach((group) => {
        let statusClass, statusText, extraInfo;
        const book = group.books[0];

        // STATUS SWITCH
        switch (group.status) {
            case "available":
                statusClass = "status-green";
                statusText = "Available";
                extraInfo = `On Shelf`;
                break;
            case "borrowed":
                statusClass = "status-orange";
                statusText = "Borrowed";
                extraInfo = book.dueDate ? `Time Borrowed: ${calculateTimeDifference(book.dueDate, false)}` : "Borrowed";
                break;
            case "overdue":
                statusClass = "status-dark-orange";
                statusText = "Overdue";
                extraInfo = book.dueDate ? `Overdue for ${calculateTimeDifference(book.dueDate, true)}` : "Overdue";
                break;
            case "lost":
                statusClass = "status-black";
                statusText = "Lost";
                if (book.damageType === 'other') {
                    extraInfo = 'Others';
                } else {
                    extraInfo = book.damageDetails || 'Reported Missing';
                }
                break;
        }

        const titleDisplay =
            group.status === "available" && group.count > 1
                ? `<span style="color:var(--accent); font-weight:bold;">(${group.count})</span> ${group.title}`
                : group.title;

        const row = `
            <tr>
                <td><div class="status-dot ${statusClass}" title="${statusText}"></div></td>
                <td>
                    <div style="font-weight:bold; font-size:16px;">${titleDisplay}</div>
                    <div style="font-size:12px; color:var(--tertiary);">${group.author}</div>
                </td>
                <td style="font-family:monospace; letter-spacing:1px; font-size: 16px;">${group.isbn}</td>
                <td style="font-size: 14px;">${extraInfo}</td>
                <td>
                    <button class="btn-icon" onclick="viewBookDetails(${group.id})"><i class="fas fa-eye"></i></button>
                </td>
            </tr>`;
        tableBody.innerHTML += row;
    });
}

