// SAVE NEW BORROWER
function saveNewBorrower() {
    const lastName = document.getElementById("new-borrower-lastname").value.trim();
    const firstName = document.getElementById("new-borrower-firstname").value.trim();
    const middleName = document.getElementById("new-borrower-middlename").value.trim();
    const fullName = middleName ? `${lastName}, ${firstName} ${middleName}` : `${lastName}, ${firstName}`;
    const studentId = document.getElementById("new-borrower-studentid").value.trim();
    const course = document.getElementById("new-borrower-course").value.trim();
    const email = document.getElementById("new-borrower-email").value.trim();
    const phone = document.getElementById("new-borrower-phone").value.trim();

    const registerBtn = document.querySelector(
        "#add-borrower-modal .btn-success",
    );

    // FIELD FILL CHECKER
    if (!lastName || !firstName || !studentId || !course || !email || !phone) {
        triggerModalError("add-borrower-modal", registerBtn, "FILL ALL FIELDS");
        return;
    }

    // NUMBER-HYPEN CHECK FOR STUDENT ID
    if (!/^[\d-]+$/.test(studentId)) {
        triggerModalError("add-borrower-modal", registerBtn, "INVALID STUDENT ID");
        return;
    }

    // EMAIL DOMAIN CHECK - must have local part before @btech.ph.education
    // Note: As of now, it only checks for valid domains but in the near future, I will add localpart validation too
    if (!/^.+@btech\.ph\.education$/.test(email)) {
        triggerModalError("add-borrower-modal", registerBtn, "INVALID EMAIL ADDRESS");
        return;
    }

    // NUMBER-HYPEN CHECK FOR PHONE NUMBER
    if (!/^[\d+-]+$/.test(phone)) {
        triggerModalError(
            "add-borrower-modal",
            registerBtn,
            "INVALID PHONE NUMBER",
        );
        return;
    }

    // DUPE Student ID CHECK
    const existingBorrower = borrowerDatabase.find(
        (b) => b.studentId === studentId,
    );
    if (existingBorrower) {
        triggerModalError(
            "add-borrower-modal",
            registerBtn,
            "STUDENT ALREADY EXISTS",
        );
        return;
    }

    // CREATE NEW BORROWER
    const newBorrower = {
        id: Date.now(),
        fullName: fullName,
        studentId: studentId,
        course: course,
        email: email,
        phone: phone,
        status: "new",
        dateAdded: new Date().toISOString().split("T")[0],
        currentBooks: [],
        pastBooks: [],
        violations: [],
    };

    borrowerDatabase.push(newBorrower);
    updateBorrowerStatus(newBorrower);
    addLog('ADD_BORROWER', `Name: ${fullName} | Student ID: ${studentId} | Course: ${course}`);
    renderBorrowerTable();
    closeModal("add-borrower-modal");
}

// BORROWER STATUS UPDATER
function updateBorrowerStatus(borrower) {
    const uniqueISBNs = new Set(borrower.pastBooks.map(book => book.isbn));
    const totalBooksRead = uniqueISBNs.size;
    borrower.status = totalBooksRead >= 20 ? "regular" : "new";
}

function updateAllBorrowersStatus() {
    borrowerDatabase.forEach(borrower => updateBorrowerStatus(borrower));
}

// SEARCH AND FILTER
function renderBorrowerTable() {
    const tableBody = document.querySelector("#borrower-table tbody");
    const searchTerm = document.getElementById("borrower-search").value.toLowerCase();
    const statusFilter = document.getElementById("borrower-status-filter").value;

    let filtered = borrowerDatabase.filter((borrower) => {
        const matchesSearch =
            borrower.fullName.toLowerCase().includes(searchTerm) ||
            borrower.studentId.toLowerCase().includes(searchTerm) ||
            borrower.course.toLowerCase().includes(searchTerm) ||
            borrower.email.toLowerCase().includes(searchTerm);

        let matchesStatus = true;
        if (statusFilter === "overdue") {
            matchesStatus = borrower.currentBooks.some((book) => {
                const dueDate = new Date(book.dueDate);
                const today = new Date();
                dueDate.setHours(0, 0, 0, 0);
                today.setHours(0, 0, 0, 0);
                return dueDate < today;
            });
        } 
        
        else if (statusFilter === "reading") {
            if (borrower.currentBooks.length === 0) {
                matchesStatus = false;
            } 
            
            else {
                const hasOverdue = borrower.currentBooks.some((book) => {
                    return isDateOverdue(book.dueDate);
                });
                matchesStatus = !hasOverdue; // Only show if NO overdue books
            }
        } 
        else if (statusFilter === "none") {
            matchesStatus = borrower.currentBooks.length === 0;
        }

        return matchesSearch && matchesStatus;
    });

    // THE SORTING MECHANISM
    filtered.sort((a, b) => {
        const aHasOverdue = a.currentBooks.some((book) => {
            return isDateOverdue(book.dueDate);
        });

        const bHasOverdue = b.currentBooks.some((book) => {
            return isDateOverdue(book.dueDate);
        });

        const aPriority = aHasOverdue ? 3 : a.currentBooks.length > 0 ? 2 : 1;
        const bPriority = bHasOverdue ? 3 : b.currentBooks.length > 0 ? 2 : 1;

        const priorityDiff = bPriority - aPriority;
        if (priorityDiff !== 0) return priorityDiff;

        return a.fullName.localeCompare(b.fullName);
    });

    tableBody.innerHTML = "";

    if (filtered.length === 0) {
        tableBody.innerHTML = `
            <tr>
                <td colspan="5" style="text-align:center; padding:40px; color:var(--tertiary);">
                    <i class="fas fa-users" style="font-size:40px; margin-bottom:10px;"></i><br>
                    No borrowers found
                </td>
            </tr>`;
        return;
    }

    filtered.forEach((borrower) => {
        const statusBadge =
            borrower.status === "new"
                ? '<span class="student-status-badge status-new-badge">NEW</span>'
                : '<span class="student-status-badge status-regular-badge">REGULAR</span>';

        // SORTING
        let borrowingStatus = "";
        let statusDot = "";
        const bookCount = borrower.currentBooks.length;

        if (bookCount === 0) {
            borrowingStatus = "No books";
            statusDot = '<div class="status-dot status-no-record"></div>';
        } else {
            const hasOverdue = borrower.currentBooks.some((book) => {
                return isDateOverdue(book.dueDate);
            });

            if (hasOverdue) {
                borrowingStatus = `<span style="color: var(--accent); font-weight:bold;">[${bookCount}]</span> Currently Reading (overdue)`;
                statusDot = '<div class="status-dot status-dark-orange"></div>';
            } else {
                borrowingStatus = `<span style="color:var(--accent); font-weight:bold;">[${bookCount}]</span> Currently Reading`;
                statusDot = '<div class="status-dot status-green"></div>';
            }
        }

        const row = `
            <tr>
                <td>${statusDot}</td>
                <td>
                    <div style="font-weight:bold; font-size:16px;">${borrower.fullName}${statusBadge}</div>
                    <div style="font-size:11px; color:var(--tertiary);">${borrower.course}</div>
                </td>
                <td style="font-family:monospace; letter-spacing:1px; font-size:16px;">${borrower.studentId}</td>
                <td style="font-size:14px;">${borrowingStatus}</td>
                <td>
                    <button class="btn-icon" onclick="viewBorrowerDetails(${borrower.id})"><i class="fas fa-eye"></i></button>
                </td>
            </tr>`;

        tableBody.innerHTML += row;
    });
}

// VIEW BORROWER DETAILS 
function viewBorrowerDetails(id) {
    const borrower = borrowerDatabase.find((b) => b.id === id);
    if (!borrower) return;
    currentViewBorrowerId = id;
    document.getElementById("view-borrower-fullname").innerText = borrower.fullName;
    document.getElementById("view-borrower-studentid").innerText = borrower.studentId;
    document.getElementById("view-borrower-course").innerText = borrower.course;
    document.getElementById("view-borrower-email").innerText = borrower.email;
    document.getElementById("view-borrower-phone").innerText = borrower.phone;
    document.getElementById("view-borrower-dateadded").innerText = borrower.dateAdded;

    const statusBadge = document.getElementById("view-borrower-status-badge");
    if (borrower.status === "new") {
        statusBadge.style.background = "var(--status-new)";
        statusBadge.style.color = "white";
        statusBadge.innerText = "NEW STUDENT";
    } else {
        statusBadge.style.background = "var(--status-green)";
        statusBadge.style.color = "white";
        statusBadge.innerText = "REGULAR STUDENT";
    }

    // PRESENT
    const currentBooksDiv = document.getElementById("view-borrower-currentbooks");
    const currentCount = borrower.currentBooks.length;

    const currentBooksParent = currentBooksDiv.parentElement;
    const currentBookLabel = currentBooksParent.querySelector(".detail-label");
    if (currentBookLabel) {
        currentBookLabel.innerHTML = `CURRENTLY READING <span style="color:var(--accent);">(${currentCount})</span>`;
    }

    if (currentCount === 0) {
        currentBooksDiv.innerHTML =
            '<p style="color:var(--tertiary); font-size: 14px;">No books currently borrowed</p>';
    } else {
        let booksHTML = '<div class="books-list">';
        borrower.currentBooks.forEach((book) => {
            const isOverdue = isDateOverdue(book.dueDate);
            const overdueClass = isOverdue
                ? 'style="color:var(--status-overdue);"'
                : "";

            booksHTML += `
                <div class="book-item">
                    <div class="book-item-title">${book.title}</div>
                    <div class="book-item-details">by ${book.author}</div>
                    <div class="book-item-details">ISBN: ${book.isbn}</div>
                    <div class="book-item-due" ${overdueClass}>Due: ${book.dueDate} ${isOverdue ? "(OVERDUE)" : ""}</div>
                </div>`;
        });
        booksHTML += "</div>";
        currentBooksDiv.innerHTML = booksHTML;
    }

    // HISTORY
    const pastBooksDiv = document.getElementById("view-borrower-pastbooks");
    const pastCount = borrower.pastBooks.length;

    const pastBooksParent = pastBooksDiv.parentElement;
    const pastBookLabel = pastBooksParent.querySelector(".detail-label");
    if (pastBookLabel) {
        pastBookLabel.textContent = `READING HISTORY (${pastCount})`;
    }

    if (pastCount === 0) {
        pastBooksDiv.innerHTML =
            '<p style="color:var(--tertiary); font-size: 14px;">No reading history</p>';
    } else {
        let booksHTML = '<div class="books-list">';
        borrower.pastBooks.forEach((book) => {
            booksHTML += `
                <div class="book-item">
                    <div class="book-item-title">${book.title}</div>
                    <div class="book-item-details">by ${book.author}</div>
                    <div class="book-item-details">ISBN: ${book.isbn}</div>
                    <div class="book-item-due">Returned: ${book.returnDate}</div>
                </div>`;
        });
        booksHTML += "</div>";
        pastBooksDiv.innerHTML = booksHTML;
    }

    // VIOLATIONS
    const violationsDiv = document.getElementById("view-borrower-violations");
    const violationCount = borrower.violations.length;
    const violationsParent = violationsDiv.parentElement;
    const violationLabel = violationsParent.querySelector(".detail-label");
    if (violationLabel) {
        violationLabel.innerHTML = `LIBRARY VIOLATIONS <span style="color:var(--accent);">(${violationCount})</span>`;
    }

    if (violationCount === 0) {
        violationsDiv.innerHTML =
            '<p style="color:var(--status-green); font-size: 14px;">No violations recorded</p>';
    } else {
        let violationsHTML = '<div class="violations-list">';
        borrower.violations.forEach((violation) => {
            let violationType = violation.type;
            if ((violation.type === "Damaged Book" || violation.type === "Book Damage") && violation.description) {
                const displayDesc = violation.damageType === 'other' ? 'Other' : violation.description;
                violationType = `${violation.type} [${displayDesc}]`;
            }
            
            violationsHTML += `
                <div class="violation-item">
                    <div class="violation-type">${violationType}</div>
                    <div class="violation-details">Book: ${violation.book}</div>
                    <div class="violation-details">Date: ${violation.date}</div>
                    ${violation.days ? `<div class="violation-details">Days Overdue: ${violation.days}</div>` : ""}
                    ${violation.status ? `<div class="violation-status">${violation.status}</div>` : ""}
                </div>`;
        });
        violationsHTML += "</div>";
        violationsDiv.innerHTML = violationsHTML;
    }

    openModal("view-borrower-modal");
}

